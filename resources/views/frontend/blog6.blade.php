              First on the list of exciting things to do in Currumbin is Currumbin Creek - a breathtaking creek between Currumbin and Palm Beach. Worth seeing alone for its picturesque scenery, Currumbin Creek boasts the best location for relaxing and getting busy in the water, with just a few popular summer holiday activities to enjoy that involve swimming, fishing and kayaking.
              
              Take a picnic to enjoy on the water’s edge then hop in a kayak to absorb the creek’s idyllic surroundings. Paddle around Currumbin Alley with a view of the Gold Coast Highway bridge or float along the creek at your own pace with lush rainforest scenery to see on either side.
              
              Currumbin Wildlife Sanctuary.</p>
            <p>Currumbin Wildlife Sanctuary is the ultimate day out on the Gold Coast! With so much to do at this leading family attraction, it’s worth getting there early to take it all in, which includes fascinating Australian wildlife to see up-close, a thrilling zip-line experience and more.
              
              Animals you can see firsthand at Currumbin Wildlife Sanctuary include koalas, kangaroos and wallabies, crocodiles and other reptiles along with birds, red pandas, wombats and many more. See the park on Segway, catch live daily shows and tuck in throughout the day with a variety of café and restaurant options to choose from. If you’re brave enough, have a go on the TreeTop Challenge, which features tall rope bridges and 11 high-speed zip-lines.
              
              For more wildlife discovery, visit Superbee Honeyworld located next to Currumbin Wildlife Sanctuary. At this Currumbin attraction, learn about the honey-making process, witness a live bee show and treat your tastebuds to some of the finest honey in all of Australia.
              
