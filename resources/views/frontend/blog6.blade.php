            <p>

              With summer coming, it's time to start planning your next holiday! Currumbin on the Gold Coast is one famous Queensland destination that offers an endless list of things to do in the summer heat, and with our cheap Currumbin Beach accommodation for families and couples, you can guarantee to be in for a memorable summer ahead.
              
              Rocks Resort is situated on Pacific Parade Currumbin, offering a choice of fully self-contained one and two-bedroom apartments with spectacular views of the ocean! In each of our apartments, feel at home from check-in with a comfortably furnished lounge and fully equipped kitchen, along with an inviting Queen bedroom, free Foxtel and a large private balcony.
              
              In the heart of the Southern Gold Coast, Currumbin presents a tropical retreat with both popular and hidden attractions to discover, tempting restaurants and cafés and a holiday atmosphere that’s as laid-back as it gets. Make Currumbin QLD your summer destination with the Rock Resort and be ready for everything it has to offer!
              
              Currumbin Creek.
